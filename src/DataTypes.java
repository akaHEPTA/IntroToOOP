public class DataTypes {
    public static void main(String[] args) {
        // 8 - primitive data types
        //   - the value(itself) gets stored in memory

        byte a = 10;     // 8 bits integer (-126 to 127)
        short b = 2019;  // 16 bits integer (-2^14 to 2^14-1)
        int c = 1000;    // 32 bits integer (-2^31 to 2^31-1)
        long d = 12012;  // 64 bits integer (-2^63 to 2^63-1)

        char e = 'a';      // 8 bits - single character
        boolean f = true;  // true <-> false or 1 <-> 0
        float g = 3.14f;   // 32 bits floating point numbers
        double h = 3.14;   // 64 bits (double-precision) floating point numbers

        // Operators
        // Arithmetic: +, -, *, /, %
        // Assignment: =
        // Comparison: >, <, >=, <=, ==, !=
        // Logical: &&(and), ||(or), !(not)
        // Increment(Decrement) by one: ++, --

    }
}
